import { client } from "@/utils/directUs";
import Footer from "./components/Footer";
import Header from "./components/Header";
import "./globals.css";
import { readItems } from "@directus/sdk";
export const metadata = {
  title: "Pemmtastic",
  description: "Generated by Next.js",
};

export const dynamic = "force-dynamic";

export default async function RootLayout({ children }) {
  const about_people = await client.request(readItems("about_people"));

  const about_mission_data = await client.request(readItems("about_mission"));

  return (
    <html lang="en">
      <head>
        <link rel="icon" href="/favicon.png" />
      </head>
      <body>
        <main>
          <div className="relative">
            <Header
              about_people={about_people}
              about_mission={about_mission_data}
              className="absolute inset-x-0"
            />
            <div className="bg-background-500">{children}</div>
          </div>
          <Footer />
        </main>
      </body>
    </html>
  );
}
